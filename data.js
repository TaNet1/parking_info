// —— 生成占位海报（SVG data URI），可指定宽高比以验证 9:16 自适应 ——
function poster(w, h, title, sub, c1, c2) {
  const svg = `<svg xmlns="http://www.w3.org/2000/svg" width="${w}" height="${h}" viewBox="0 0 ${w} ${h}">
    <defs>
      <linearGradient id="g" x1="0" y1="0" x2="1" y2="1">
        <stop offset="0" stop-color="${c1}"/><stop offset="1" stop-color="${c2}"/>
      </linearGradient>
    </defs>
    <rect width="${w}" height="${h}" fill="url(#g)"/>
    <text x="50%" y="44%" fill="#fff" font-size="${Math.round(w*0.10)}"
      font-family="PingFang SC, sans-serif" font-weight="700"
      text-anchor="middle">${title}</text>
    <text x="50%" y="54%" fill="rgba(255,255,255,.85)" font-size="${Math.round(w*0.05)}"
      font-family="PingFang SC, sans-serif" text-anchor="middle">${sub}</text>
    <text x="50%" y="92%" fill="rgba(255,255,255,.7)" font-size="${Math.round(w*0.038)}"
      font-family="monospace" text-anchor="middle">${w}×${h}</text>
  </svg>`;
  return 'data:image/svg+xml;charset=utf-8,' + encodeURIComponent(svg);
}

